const { solution } = require('../15651/solution');

describe('15651 N과 M(3) (실버3)', () => {
    test('test 1', () => {
        expect(
            solution(3, 1)
        ).toEqual([
            '1',
            '2',
            '3'
        ].join('\n'))
    })

    test('test 2', () => {
        expect(
            solution(4, 2)
        ).toEqual([
            '1 1',
            '1 2',
            '1 3',
            '1 4',
            '2 1',
            '2 2',
            '2 3',
            '2 4',
            '3 1',
            '3 2',
            '3 3',
            '3 4',
            '4 1',
            '4 2',
            '4 3',
            '4 4'
        ].join('\n'))
    })

    test('test 3', () => {
        expect(
            solution(3, 3)
        ).toEqual([
            '1 1 1',
            '1 1 2',
            '1 1 3',
            '1 2 1',
            '1 2 2',
            '1 2 3',
            '1 3 1',
            '1 3 2',
            '1 3 3',
            '2 1 1',
            '2 1 2',
            '2 1 3',
            '2 2 1',
            '2 2 2',
            '2 2 3',
            '2 3 1',
            '2 3 2',
            '2 3 3',
            '3 1 1',
            '3 1 2',
            '3 1 3',
            '3 2 1',
            '3 2 2',
            '3 2 3',
            '3 3 1',
            '3 3 2',
            '3 3 3'
        ].join('\n'))
    })
})